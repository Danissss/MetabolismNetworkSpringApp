package com.example.MetabolismNetwork.Model;

public class CypReactModel {
	
	private String transporterName;
	private String transporterRole;
	private String query;
	
	public String getTransporterName() {
		return transporterName;
	}
	
	public void setTransporterName(String transporterName) {
		this.transporterName = transporterName; 
	}
	
	public String getTransporterRole() {
		return transporterRole;
	}
	
	public void setTransporterRole(String transporterRole) {
		this.transporterRole = transporterRole;
	}
	
	public String getQuery() {
		return query;
	}
	
	public void setQuery(String query) {
		this.query = query;
	}
	
	
}
